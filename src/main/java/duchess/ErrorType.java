package duchess;

/**
 * The {@code ErrorType} enum represents the different types of errors that
 * can occur in the Duchess application.
 */
public enum ErrorType {
    INVALID_INDEX,
    INVALID_COMMAND,
    INVALID_FORMAT,
    INVALID_FORMAT_TODO,
    INVALID_FORMAT_DEADLINE,
    INVALID_FORMAT_EVENT,
    UNKNOWN_ERROR
}
